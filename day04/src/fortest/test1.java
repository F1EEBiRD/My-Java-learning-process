package fortest;

public class test1 {
    public static void main(String[] args) {
        //for(初始化语句; 条件判断语句; 条件控制语句){}
        for(int i = 1; i <= 5; i++){
            System.out.println(i);

            //需求2：打印5-1
            for(int j = 5; j >= 1; j--){
                System.out.println(j);
            }


        }
    }
}
